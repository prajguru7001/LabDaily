package Assignment;

public class Swap1{

    public static void main(String [] args){

        int a, b, temp;

        a = 20; b = 30;

        System.out.println("The two numbers are: a = "+a+" and b = "+b+".");

        temp = a;
        a = b;
        b = temp;

        System.out.println("Swaped numbers are: a = "+a+" and b = "+b+".");

    }

}
